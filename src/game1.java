	private static int call(int left, int right, int[] arr, boolean isFirst, int[][] dp) {
		if(left == arr.length) {
			return 0;
		}
		if(right == -1) {
			return 0;
		}
		if(left == right) {
			if(isFirst) {
				return dp[left][right] = arr[left];
			}
			else {
				return dp[left][right] = 0;
			}
		}
		if(dp[left][right] != -1) {
			return dp[left][right];
		}
		
		if(isFirst) {
			isFirst = false;
			int res1 = arr[left] + call(left + 1, right, arr, isFirst, dp);
			int res2 = arr[right] + call(left, right - 1, arr, isFirst, dp);
			return dp[left][right] =  max(res1, res2);
		}
		else {
			isFirst = true;
			int res1 = call(left + 1, right, arr, isFirst, dp);
			int res2 = call(left, right - 1, arr, isFirst, dp);
			return dp[left][right] = min(res1, res2);
		}
	}

